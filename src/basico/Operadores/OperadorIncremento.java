package basico.Operadores;

public class OperadorIncremento {

    public static void main(String[] args) {

        int valorBase =10;

        //Apos utilizar  varivel ele soma mais 1
        valorBase++;
        System.out.println(valorBase);

        //antes de utilizar variavel ja soma 1
        ++valorBase;
        System.out.println(valorBase);

        //Apos utilizar  varivel ele diminui 1
        valorBase--;
        System.out.println(valorBase);

        //antes de utilizar variavel ja diminui 1
        --valorBase;
        System.out.println(valorBase);

    }
}
